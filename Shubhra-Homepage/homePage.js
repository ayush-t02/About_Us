function showAns1() {
    var x = document.getElementById("ans1");
    var y = document.getElementById("show-ans1");
    if (x.style.display === "block") {
        x.style.display = "none";
        y.style.transform = "rotate(0deg)";
    } else {
        x.style.display = "block";
        y.style.transform = "rotate(45deg)";
    }
}

